    /**
     * Load a template file (with caching)
     * @param {string} templatePath - Path relative to prompts directory
     * @returns {Promise<string>} Template content
     */
    async function loadTemplate(templatePath) {
      if (injectedTemplates) {
        const t = injectedTemplates[templatePath];
        if (t != null) return t;
        throw new Error(`Template not in injected map: ${templatePath}`);
      }
      if (templateCache.has(templatePath)) {
        return templateCache.get(templatePath);
      }

      const fullPath = `/js/ai/prompts/${templatePath}`;

      try {
        const response = await fetch(fullPath);

        if (!response.ok) {
          throw new Error(`HTTP ${response.status}: ${response.statusText}`);
        }

        const content = await response.text();
        templateCache.set(templatePath, content);

        return content;
      } catch (error) {

        throw new Error(`Failed to load template: ${templatePath}`);
      }
    }

    /**
     * Fill template placeholders with values
     * @param {string} template - Template string with {{PLACEHOLDER}} syntax
     * @param {Object} values - Key-value pairs for replacement
     * @returns {string} Filled template
     */
    function fillTemplate(template, values) {
      return template.replace(/\{\{(\w+)\}\}/g, (match, key) => {
        if (values[key] !== undefined && values[key] !== null) {
          return values[key];
        }
        // Leave placeholder if no value provided

        return '';
      });
    }

