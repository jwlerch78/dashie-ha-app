    /**
     * Build complete prompt for AI
     * @param {Object} options
     * @param {string} options.userRequest - User's voice/text input
     * @param {string} options.inquiryType - 'calendar-events' | 'family-members' | 'web-search' | null
     * @param {Object} options.retrievedData - Data from info request (if any)
     * @param {Object} options.context - Additional context values (including personalityMode)
     * @returns {Promise<string>} Complete prompt
     */
    async function buildPrompt({ userRequest, inquiryType, retrievedData, context = {} }) {

      const dateTime = formatDateTime();

      // Check for personality mode (e.g., 'santa')
      // Custom personality config takes precedence over built-in modes
      let personalityConfig = null;

      if (context.customPersonalityConfig) {
        // Build prompt from structured personality fields
        const { responsePrefix, responseSuffix } = buildPersonalityPrompt(context.customPersonalityConfig);
        personalityConfig = {
          name: context.customPersonalityConfig.name,
          responsePrefix,
          responseSuffix
        };

      }

      // Generate available tools list for prompt (platform-aware)
      const availableToolsList = generateAvailableToolsList();

      // Language: resolve from account-wide Preferences. 'system' or absent
      // means follow the model's default — we omit the instruction entirely
      // so we don't waste tokens on a no-op line.
      //
      // Source priority:
      //   1. context.language (Console passes from its cached user_settings)
      //   2. window.settingsStore (webapp dashboard hydrates this at boot)
      //   3. 'system' (no instruction emitted)
      const languageCode = context.language
        || (typeof window !== 'undefined' && window.settingsStore?.get?.('general.language'))
        || 'system';
      const languageInstruction = (languageCode && languageCode !== 'system')
        ? `Respond in ${languageNameFor(languageCode)}.`
        : '';

      // Base values for all templates
      const baseValues = {
        DATE_TIME: dateTime,
        USER_REQUEST: userRequest,
        CHAT_HISTORY: context.chatHistory || '',  // Empty for now, implement later
        AVAILABLE_TOOLS_LIST: availableToolsList,
        LANGUAGE_INSTRUCTION: languageInstruction,
        ...context
      };

      // Load and fill base context
      const baseTemplate = await loadTemplate('base-context.md');
      let prompt = fillTemplate(baseTemplate, baseValues);

      // Add personality prefix if applicable (prepend to existing prompt)
      if (personalityConfig) {
        const personalityPrefix = personalityConfig.responsePrefix || '';
        prompt = personalityPrefix + '\n\n' + prompt;

      }

      // Add inquiry-specific context if we have retrieved data
      if (inquiryType && retrievedData) {
        try {
          const inquiryTemplate = await loadTemplate(`inquiries/${inquiryType}.md`);
          const inquiryValues = buildInquiryValues(inquiryType, retrievedData, baseValues);
          prompt += '\n\n' + fillTemplate(inquiryTemplate, inquiryValues);
        } catch (error) {

        }

        // With retrieved data, use the full response format (includes all display flags)
        const responseFormat = await loadTemplate('response-format.md');
        prompt += '\n\n' + fillTemplate(responseFormat, baseValues);
      } else {
        // Initial request (no data yet) - use slim format focused on tool selection.
        // fillTemplate is essential here — response-format-initial.md references
        // {{AVAILABLE_TOOLS_LIST}}, and without substitution the AI sees the raw
        // placeholder and can't pick a tool. (Bug hidden until LANGUAGE_INSTRUCTION
        // landed on base-context.md — same surface, easier to spot.)
        const responseFormat = await loadTemplate('response-format-initial.md');
        prompt += '\n\n' + fillTemplate(responseFormat, baseValues);
      }

      // Add personality suffix if applicable (append after response format)
      if (personalityConfig && personalityConfig.responseSuffix) {
        prompt += personalityConfig.responseSuffix;
      }

      return prompt;
    }

